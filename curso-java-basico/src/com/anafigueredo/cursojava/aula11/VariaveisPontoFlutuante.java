package com.anafigueredo.cursojava.aula11;

public class VariaveisPontoFlutuante {

	public static void main(String[] args) {
        
		
	 	        // double d1 = 123.4;
				// double d2 = 1.234e2;  mesmo valor que d1, mas em notação científica
			
		double valorPassagem = 2.90; // double e float aceitam notação científica 
		
		// float f1 = 123.4f; notação cientifica 
		float valorTomate = 3.95f;  // f distingue o float do double 
		System.out.println("Valor da passagem = " + valorPassagem);
		System.out.println("Valor do tomate = " + valorTomate);

		
	}

}
